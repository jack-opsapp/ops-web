// ─── In-memory Supabase double ────────────────────────────────────────────────

interface DbState {
  aiDraftHistory: Array<Record<string, unknown>>;
  emailConnections: Array<Record<string, unknown>>;
  notifications: Array<Record<string, unknown>>;
}

/**
 * Extended double that handles the additional query patterns needed by T11:
 *   - .not(col, "is", null) — filter rows where col IS NOT NULL
 *   - .limit(n) — return at most n rows
 *
 * Existing patterns from auto-draft-mailbox.test.ts are preserved.
 */
function makeSupabaseDouble(state: DbState) {
  class Query {
    private _table: string;
    private _action: "select" | "insert" | "update" = "select";
    private _payload: Record<string, unknown> | null = null;
    private _filters = new Map<string, unknown>();
    private _notNullCols: string[] = [];
    private _limitN: number | null = null;

    constructor(table: string) {
      this._table = table;
    }

    select(_cols?: string) { return this; }

    eq(col: string, val: unknown) {
      this._filters.set(col, val);
      return this;
    }

    not(col: string, _op: string, _val: unknown) {
      // Only "is null" / "is not null" patterns are used here.
      // Record the column as a "must not be null" filter.
      this._notNullCols.push(col);
      return this;
    }

    limit(n: number) {
      this._limitN = n;
      return this;
    }

    insert(payload: Record<string, unknown>) {
      this._action = "insert";
      this._payload = payload;
      if (this._table === "notifications") {
        state.notifications.push(payload);
      }
      return this;
    }

    update(payload: Record<string, unknown>) {
      this._action = "update";
      this._payload = payload;
      return this;
    }

    private _resolve(): { data: unknown; error: null } {
      if (this._table === "email_connections" && this._action === "select") {
        const id = this._filters.get("id");
        const companyId = this._filters.get("company_id");
        const row =
          state.emailConnections.find(
            (c) => c.id === id && (!companyId || c.company_id === companyId)
          ) ?? null;
        return { data: row, error: null };
      }
      if (this._table === "ai_draft_history" && this._action === "select") {
        const connId = this._filters.get("connection_id");
        const threadId = this._filters.get("thread_id");

        let rows = state.aiDraftHistory.filter((r) => {
          if (connId !== undefined && r.connection_id !== connId) return false;
          if (threadId !== undefined && r.thread_id !== threadId) return false;
          // Apply .not("col", "is", null) — keep only rows where col is not null
          for (const col of this._notNullCols) {
            if (r[col] === null || r[col] === undefined) return false;
          }
          return true;
        });

        if (this._limitN !== null) {
          rows = rows.slice(0, this._limitN);
        }

        return { data: rows, error: null };
      }
      if (this._table === "ai_draft_history" && this._action === "update") {
        const id = this._filters.get("id");
        const row = state.aiDraftHistory.find((r) => r.id === id);
        if (row && this._payload) Object.assign(row, this._payload);
        return { data: null, error: null };
      }
      if (this._action === "insert" || this._action === "update") {
        return { data: null, error: null };
      }
      return { data: null, error: null };
    }

    then<T1 = unknown, T2 = never>(
      onFulfilled?: ((val: unknown) => T1 | PromiseLike<T1>) | null,
      onRejected?: ((reason: unknown) => T2 | PromiseLike<T2>) | null
    ) {
      return Promise.resolve(this._resolve()).then(onFulfilled, onRejected);
    }

    async single() {
      return this._resolve() as { data: Record<string, unknown> | null; error: null };
    }
  }

  return {
    from: (table: string) => new Query(table),
  };
}

