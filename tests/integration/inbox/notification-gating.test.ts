// ─── Tests ────────────────────────────────────────────────────────────────────

describe("T11 — AUTO_DRAFT_READY notification gating", () => {
  it("does NOT insert AUTO_DRAFT_READY notification when inbox_ui is OFF", async () => {
    const state = makeBaseState();

    getProviderMock.mockReturnValue({
      createDraft: vi.fn().mockResolvedValue("provider-draft-id"),
      updateDraft: vi.fn(),
    });

    isFeatureEnabledMock.mockResolvedValue(false); // inbox hidden

    setSupabaseOverride(makeSupabaseDouble(state) as never);

    await maybeAutoGenerateDraft(makeInboundEmail(), makeConnection(), "opp-1");

    const draftReadyNotifs = state.notifications.filter(
      (n) => n.title === "Draft ready"
    );
    expect(draftReadyNotifs).toHaveLength(0);
  });

  it("inserts AUTO_DRAFT_READY notification when inbox_ui is ON", async () => {
    const state = makeBaseState();

    getProviderMock.mockReturnValue({
      createDraft: vi.fn().mockResolvedValue("provider-draft-id"),
      updateDraft: vi.fn(),
    });

    isFeatureEnabledMock.mockResolvedValue(true); // inbox visible

    setSupabaseOverride(makeSupabaseDouble(state) as never);

    await maybeAutoGenerateDraft(makeInboundEmail(), makeConnection(), "opp-1");

    const draftReadyNotifs = state.notifications.filter(
      (n) => n.title === "Draft ready"
    );
    expect(draftReadyNotifs).toHaveLength(1);
    expect(draftReadyNotifs[0].action_url).toBe("/inbox");
    expect(draftReadyNotifs[0].type).toBe("ai_milestone");
  });
});

describe("T11 — one-time draft explainer notification", () => {
  it("inserts explainer on first mailbox draft when inbox_ui is OFF", async () => {
    const state = makeBaseState();
    // No prior rows with mailbox_draft_id — this is the first placement

    getProviderMock.mockReturnValue({
      createDraft: vi.fn().mockResolvedValue("provider-draft-id"),
      updateDraft: vi.fn(),
    });

    isFeatureEnabledMock.mockResolvedValue(false); // inbox hidden

    setSupabaseOverride(makeSupabaseDouble(state) as never);

    await maybeAutoGenerateDraft(makeInboundEmail(), makeConnection(), "opp-1");

    const explainerNotifs = state.notifications.filter(
      (n) => n.title === "Replies, pre-drafted"
    );
    expect(explainerNotifs).toHaveLength(1);
    expect(explainerNotifs[0]).toMatchObject({
      user_id: "user-1",
      company_id: "company-1",
      type: "ai_milestone",
      persistent: false,
      action_url: "/settings?tab=integrations",
      action_label: "Email settings",
    });
  });

  it("does NOT insert explainer on second mailbox draft (dedup)", async () => {
    const state: DbState = {
      aiDraftHistory: [
        // Prior successful mailbox draft exists for this connection
        {
          id: "draft-history-prior",
          connection_id: "conn-1",
          thread_id: "thread-prior",
          status: "auto_drafted",
          mailbox_draft_id: "existing-provider-draft",
        },
        // New history row for current draft
        {
          id: "draft-history-2",
          connection_id: "conn-1",
          thread_id: "thread-1",
          status: "pending",
          mailbox_draft_id: null,
        },
      ],
      emailConnections: [
        {
          id: "conn-1",
          company_id: "company-1",
          auto_send_settings: DRAFT_SETTINGS,
        },
      ],
      notifications: [],
    };

    generateDraftMock.mockResolvedValue({
      available: true,
      draft: "Second draft.",
      draftHistoryId: "draft-history-2",
    });

    getProviderMock.mockReturnValue({
      createDraft: vi.fn().mockResolvedValue("new-provider-draft"),
      updateDraft: vi.fn(),
    });

    isFeatureEnabledMock.mockResolvedValue(false); // inbox hidden

    setSupabaseOverride(makeSupabaseDouble(state) as never);

    await maybeAutoGenerateDraft(makeInboundEmail(), makeConnection(), "opp-1");

    const explainerNotifs = state.notifications.filter(
      (n) => n.title === "Replies, pre-drafted"
    );
    expect(explainerNotifs).toHaveLength(0);
  });

  it("does NOT insert explainer when inbox_ui is ON", async () => {
    const state = makeBaseState();

    getProviderMock.mockReturnValue({
      createDraft: vi.fn().mockResolvedValue("provider-draft-id"),
      updateDraft: vi.fn(),
    });

    isFeatureEnabledMock.mockResolvedValue(true); // inbox visible

    setSupabaseOverride(makeSupabaseDouble(state) as never);

    await maybeAutoGenerateDraft(makeInboundEmail(), makeConnection(), "opp-1");

    const explainerNotifs = state.notifications.filter(
      (n) => n.title === "Replies, pre-drafted"
    );
    expect(explainerNotifs).toHaveLength(0);
  });

  it("does NOT insert explainer when the mailbox path is updateDraft (existing draft reused)", async () => {
    // When an existing mailbox draft is reused via updateDraft, we don't fire
    // the explainer — the user already received it on the createDraft call.
    const state: DbState = {
      aiDraftHistory: [
        {
          id: "draft-history-prior",
          connection_id: "conn-1",
          thread_id: "thread-1",
          status: "auto_drafted",
          mailbox_draft_id: "existing-provider-draft",
        },
        {
          id: "draft-history-1",
          connection_id: "conn-1",
          thread_id: "thread-1",
          status: "pending",
          mailbox_draft_id: null,
        },
      ],
      emailConnections: [
        {
          id: "conn-1",
          company_id: "company-1",
          auto_send_settings: DRAFT_SETTINGS,
        },
      ],
      notifications: [],
    };

    getProviderMock.mockReturnValue({
      createDraft: vi.fn().mockResolvedValue("new-draft"),
      updateDraft: vi.fn().mockResolvedValue(undefined),
    });

    isFeatureEnabledMock.mockResolvedValue(false); // inbox hidden

    setSupabaseOverride(makeSupabaseDouble(state) as never);

    await maybeAutoGenerateDraft(makeInboundEmail(), makeConnection(), "opp-1");

    const explainerNotifs = state.notifications.filter(
      (n) => n.title === "Replies, pre-drafted"
    );
    // updateDraft path: explainer not triggered (only fires from createDraft branch)
    expect(explainerNotifs).toHaveLength(0);
  });
});
